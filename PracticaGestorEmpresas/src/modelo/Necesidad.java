package modelo;

public class Necesidad {
	private int idNecesidad;
	private int idEmpresa;
	private String convocatoria;
	private int cantidadDam;
	private int cantidadDaw;
	private int cantidadAsir;
	private int cantidadMarketing;
	private int cantidadFinazas;
	public int getIdNecesidad() {
		return idNecesidad;
	}
	public void setIdNecesidad(int idNecesidad) {
		this.idNecesidad = idNecesidad;
	}
	public int getIdEmpresa() {
		return idEmpresa;
	}
	public void setIdEmpresa(int idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	public String getConvocatoria() {
		return convocatoria;
	}
	public void setConvocatoria(String convocatoria) {
		this.convocatoria = convocatoria;
	}
	public int getCantidadDam() {
		return cantidadDam;
	}
	public void setCantidadDam(int cantidadD) {
		this.cantidadDam = cantidadD;
	}
	public int getCantidadDaw() {
		return cantidadDaw;
	}
	public void setCantidadDaw(int cantidadDaw) {
		this.cantidadDaw = cantidadDaw;
	}
	public int getCantidadAsir() {
		return cantidadAsir;
	}
	public void setCantidadAsir(int cantidadAsir) {
		this.cantidadAsir = cantidadAsir;
	}
	public int getCantidadMarketing() {
		return cantidadMarketing;
	}
	public void setCantidadMarketing(int cantidadMarketing) {
		this.cantidadMarketing = cantidadMarketing;
	}
	public int getCantidadFinazas() {
		return cantidadFinazas;
	}
	public void setCantidadFinazas(int cantidadFinazas) {
		this.cantidadFinazas = cantidadFinazas;
	}
}
